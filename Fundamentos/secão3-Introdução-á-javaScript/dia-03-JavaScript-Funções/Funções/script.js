let saldo = 1100;

function multiplicaSaldo(valor) {
    return valor * saldo;
};
function divideSaldo(valor) {
    return saldo / valor;
};
function somaSaldo(valor) {
    return valor + saldo;
};
function subtraiSaldo(valor) {
    return saldo - valor; 
};
console.log(somaSaldo(700));
console.log(divideSaldo(10));
console.log(subtraiSaldo(100));
console.log(multiplicaSaldo(4));